package CoreJAVA.episode_2;

public class DataTypes {
    public static void main(String[] args) {
        //Primitive DataTypes:
        //1. Int
        int i = 10;
        int j=-10;
        i = 21;
        j=-21;
        System.out.println(i +""+j);
        //2. double
        double a = 21.08;
        double b = 21.99;
        a = 21.0899;
        b = 21.81999;
        System.out.println(a+""+b);
        //3. char
        char x ='d';
        char y ='d';
        y = 'x';
        x = 'y';
        System.out.println(x+""+y);
        //4. boolean
        boolean bool1 = true;
        boolean bool2 = false;
        System.out.println(bool1+""+bool2);
        //5. String
        String s1 = "James";
        String s2 = "Howlett";
        System.out.println(s1+""+s2);
    }
}
